<?php
function somme($a,$b)
{
    return $a+$b;
}
function exponentiel($a,$b)
{
    return pow($a,$b);
}
function difference($a,$b)
{
    return $a-$b;
}
function produit($a,$b)
{
    return $a*$b;
}
function modulo($a,$b)
{
    return $a%$b;
}
function division($a,$b)
{
    return $a/$b;
}
function carre($a)
{
    return pow($a,2);
}
?>